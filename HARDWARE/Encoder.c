int16_t Encoder_Get(void)
{
	int16_t Temp;
	Temp = TIM_GetCounter(TIM4);
	TIM_SetCounter(TIM4, 0);
	return Temp;
}

void TIM2_IRQHandler (void)
{
	if(TIM_GetITStatus(TIM2, TIM_IT_Update) != RESET)
	{
		Speed = Encoder_Get() * 60 / 8;
		TIM_ClearITPendingBit(TIM2, TIM_IT_Update);
		
	}	
}

